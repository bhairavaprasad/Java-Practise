package com.tss.test.fundamental;

/**
 * Hello World is a classic sample to start when we learn a new programming
 * language. Below is the Java version of Hello World program, it simple enough
 * to start. The code contains one class called HelloWorld, a main(String[]
 * args) method which is the execution entry point of every Java application and
 * a single line of code that write a Hello World string to the console. That's
 * all, we are done! To run the application we need to compile it first. I
 * assume that you have your Java in your path. To compile it type % javac
 * HelloWorld.java The compilation process will result a file called
 * HelloWorld.class, this is the binary version of our program. As you can see
 * that the file ends with .class extension because Java is everyting about
 * class. To run it type the command bellow, class name is written without it
 * extension. % java HelloWorld
 */

public class HelloWorld
{
	public static void main(String[] args)
	{
		// say hello to the world
		System.out.println("Hello World!");
	}
}
